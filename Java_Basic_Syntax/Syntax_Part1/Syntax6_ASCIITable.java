package Syntax_Part1;

public class Syntax6_ASCIITable {
	public static void main(String[] args) {
		
		char ch = 'a';
		int num = 97;
		
		System.out.println("ch value is " +ch+", ASCII code : "+(int)ch);
		System.out.println("num value is " + num + ", Char : " + (char)num);
		
		System.out.println("ch+1 is "+(ch+1)+", char : "+(char)(ch+1));
		System.out.println("num+1 is "+(num+1)+", char : "+(char)(num+1));
		
	}

}
