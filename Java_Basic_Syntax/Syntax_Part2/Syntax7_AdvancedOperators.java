package Syntax_Part2;

public class Syntax7_AdvancedOperators {
	public static void main(String[] args) {
		
		int num1 = 5;
		int sum = 6;
		
		sum += num1;
		System.out.println("The sum is : " + sum);
		
		sum -= num1;
		System.out.println("The sum is : " + sum);
		
		sum *= num1;
		System.out.println("The sum is : " + sum);
		
		sum /= num1;
		System.out.println("The sum is : " + sum);
		
		sum %= num1;
		System.out.println("The sum is : " + sum);
		
		
	}
}


